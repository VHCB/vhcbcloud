        public static void AddEntityNotes(int EntityID, int LkCategory, string UserName, string Notes, string URL)
        {
            try
            {
                using (SqlConnection connection = new SqlConnection(ConfigurationManager.ConnectionStrings["dbConnection"].ConnectionString))
                {
                    connection.Open();

                    using (SqlCommand command = new SqlCommand())
                    {
                        command.Connection = connection;
                        command.CommandType = CommandType.StoredProcedure;
                        command.CommandText = "AddEntityNotes";

                        //5 Parameters 
                        command.Parameters.Add(new SqlParameter("EntityID", EntityID));
                        command.Parameters.Add(new SqlParameter("LkCategory", LkCategory));
                        command.Parameters.Add(new SqlParameter("UserName", UserName));
                        command.Parameters.Add(new SqlParameter("Notes", Notes));
                        command.Parameters.Add(new SqlParameter("URL", URL));
                        command.CommandTimeout = 60 * 5;

                        command.ExecuteNonQuery();
                    }
                }
            }
            catch (Exception ex)
            {
                throw ex;
            }
        }

